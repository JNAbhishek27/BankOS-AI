let apiCooldownUntil = 0;

// A helper function to call Gemini safely, with fallback to highly intelligent rule-based templates if API Client is unavailable
async function queryAgent(agentId: AgentId, systemPrompt: string, userPrompt: string): Promise<{ text: string; simulated?: boolean }> {
  if (ai) {
    if (Date.now() < apiCooldownUntil) {
      console.log(`BankOS AI: API is in cooldown mode. Directing agent ${agentId} to high-fidelity simulated backup engine.`);
      return { text: getSimulatedAgentResponse(agentId, userPrompt), simulated: true };
    }
    try {
      const response = await ai.models.generateContent({
        model: "gemini-3.5-flash",
        contents: userPrompt,
        config: {
          systemInstruction: systemPrompt,
          temperature: 0.2,
        },
      });
      return { text: response.text || "No response text generated by AI.", simulated: false };
    } catch (error: any) {
      const errorMessage = error?.message || String(error);
      const isQuotaError = errorMessage.includes("429") || errorMessage.includes("quota") || errorMessage.includes("RESOURCE_EXHAUSTED");
      
      if (isQuotaError) {
        apiCooldownUntil = Date.now() + 45000; // 45 seconds cooldown
        console.warn(`BankOS AI [Quota Warning]: Quota exceeded (429) for agent ${agentId}. Commencing 45s simulation-only cooldown.`);
      } else {
        console.error(`Gemini call failed for agent ${agentId}:`, error);
      }
      // Fallback to local intelligence if rate-limited or transient failure
    }
  }

  // High-fidelity fallback logic simulating realistic AI agent outputs
  return { text: getSimulatedAgentResponse(agentId, userPrompt), simulated: true };
}

// Main AI chat endpoint
app.post("/api/chat", async (req, res) => {
  const { message, history, activeAgentId } = req.body;
  
  if (!message) {
    return res.status(400).json({ error: "Message is required." });
  }

  const selectedAgent = mockAgents.find((a) => a.id === activeAgentId) || mockAgents.find((a) => a.id === AgentId.ORCHESTRATOR)!;
  const systemPrompt = selectedAgent.systemPrompt + "\nFormat your output nicely with markdown. Speak directly and professionally as a senior bank intelligence expert.";

  // Build simple chat context from history
  const contextHistory = Array.isArray(history)
    ? history.slice(-6).map((h: any) => `${h.sender === "user" ? "User" : "Agent"}: ${h.text}`).join("\n")
    : "";
  
  const userPrompt = `${contextHistory}\nUser says: ${message}\nAgent response:`;

  try {
    const result = await queryAgent(selectedAgent.id, systemPrompt, userPrompt);
    res.json({
      text: result.text,
      agentId: selectedAgent.id,
      agentName: selectedAgent.name,
      simulated: !!result.simulated
    });
  } catch (err: any) {
    res.status(500).json({ error: err.message || "Internal server error" });
  }
});

// Advanced multi-agent orchestration workflow endpoint
app.post("/api/orchestrate", async (req, res) => {
  const { customerId, type, meta } = req.body;
  
  const customer = mockCustomers.find((c) => c.id === customerId);
  if (!customer) {
    return res.status(404).json({ error: "Customer not found." });
  }

  const transactions = mockTransactions.filter((t) => t.customerId === customerId);
  const documents = mockDocuments.filter((d) => d.customerId === customerId);

  console.log(`Starting BankOS Multi-Agent Workflow: ${type} request for customer ${customer.name}`);

  // Create individual agent context summaries
  const customerSummary = JSON.stringify({
    name: customer.name,
    creditScore: customer.creditScore,
    monthlyIncome: customer.monthlyIncome,
    existingDebt: customer.existingDebt,
    employmentStatus: customer.employmentStatus,
    kycStatus: customer.kycStatus,
    pepStatus: customer.pepStatus,
    address: customer.address
  }, null, 2);

  const txSummary = JSON.stringify(transactions.map(t => ({
    amount: t.amount,
    category: t.category,
    description: t.description,
    type: t.type,
    location: t.location,
    riskLevel: t.riskLevel,
    flagReason: t.flagReason,
    status: t.status
  })), null, 2);

  const docSummary = JSON.stringify(documents.map(d => ({
    type: d.type,
    fileName: d.fileName,
    status: d.status,
    ocrConfidence: d.ocrConfidence,
    issues: d.issues,
    extractedData: d.extractedData
  })), null, 2);

  const metaSummary = JSON.stringify(meta || {}, null, 2);

  // Define which agents participate in what workflows
  const participatingAgents: AgentId[] = [];
  if (type === "loan") {
    participatingAgents.push(AgentId.LOAN_OFFICER, AgentId.RISK_ANALYST, AgentId.DOCUMENT_INTELLIGENCE, AgentId.CUSTOMER_RELATIONSHIP);
  } else if (type === "compliance_audit") {
    participatingAgents.push(AgentId.COMPLIANCE, AgentId.DOCUMENT_INTELLIGENCE, AgentId.FRAUD_DETECTION);
  } else if (type === "fraud_investigation") {
    participatingAgents.push(AgentId.FRAUD_DETECTION, AgentId.RISK_ANALYST, AgentId.COMPLIANCE);
  } else {
    // default/onboarding
    participatingAgents.push(AgentId.COMPLIANCE, AgentId.DOCUMENT_INTELLIGENCE, AgentId.CUSTOMER_RELATIONSHIP);
  }

  const agentReports: Record<string, any> = {};
  const steps: WorkflowStep[] = [];

  // Run the agent queries (can be executed in parallel for performance)
  const agentPromises = participatingAgents.map(async (agentId) => {
    const agent = mockAgents.find(a => a.id === agentId)!;
    
    let promptText = `
      Perform analysis for the following Customer Request:
      - WORKFLOW TYPE: ${type.toUpperCase()}
      - REQUEST METADATA: ${metaSummary}

      CUSTOMER PORTFOLIO DATA:
      ${customerSummary}

      CUSTOMER REAL-TIME TRANSACTIONS:
      ${txSummary}

      CUSTOMER UPLOADED REGULATORY DOCUMENTS:
      ${docSummary}

      Instructions for your report:
      1. Provide a final recommendation: Choose from [approve], [deny], [flag], [neutral]. Include the choice exactly in brackets.
      2. Set a confidence level score between 0 and 100.
      3. List 2 to 4 bulleted findings.
      4. Format your report clearly using Markdown.
    `;

    const result = await queryAgent(agentId, agent.systemPrompt, promptText);
    
    // Parse the recommendation and confidence score from the text
    const textLower = result.text.toLowerCase();
    let recommendation: "approve" | "deny" | "flag" | "neutral" = "neutral";
    if (textLower.includes("[approve]")) recommendation = "approve";
    else if (textLower.includes("[deny]")) recommendation = "deny";
    else if (textLower.includes("[flag]")) recommendation = "flag";

    // Extract confidence (or default to 85)
    const confidenceMatch = result.text.match(/confidence.*(\d{2})/i) || result.text.match(/score.*(\d{2})/i);
    const confidence = confidenceMatch ? parseInt(confidenceMatch[1], 10) : 85;

    // Extract bullet points as findings
    const findings: string[] = [];
    const lines = result.text.split("\n");
    for (const line of lines) {
      if (line.trim().startsWith("-") || line.trim().startsWith("*")) {
        findings.push(line.replace(/^[-*\s]+/, "").trim());
      }
    }
    if (findings.length === 0) {
      findings.push(`Completed analysis on ${type} request.`);
      findings.push(`Identified key status as ${recommendation.toUpperCase()}.`);
    }

    agentReports[agentId] = {
      recommendation,
      confidence,
      findings: findings.slice(0, 4),
      details: result.text
    };

    steps.push({
      id: `step-${agentId}-${Date.now()}`,
      agentId,
      agentName: agent.name,
      timestamp: new Date().toLocaleTimeString(),
      action: `Completed ${type} assessment`,
      recommendation,
      details: findings[0] || "Completed analysis report."
    });
  });

  await Promise.all(agentPromises);

  // Now feed all agent reports to the ORCHESTRATOR agent to synthesize the final explainable decision!
  const orchestratorAgent = mockAgents.find(a => a.id === AgentId.ORCHESTRATOR)!;
  const reportsPayload = JSON.stringify(agentReports, null, 2);

  const orchestratorPrompt = `
    You are the central BankOS Multi-Agent Orchestrator. You must synthesize the specialized agent assessments for the following customer request:
    - CUSTOMER: ${customer.name} (Credit Score: ${customer.creditScore}, Monthly Income: INR ${customer.monthlyIncome})
    - REQUEST TYPE: ${type.toUpperCase()}
    - DETAILS: ${metaSummary}

    SPECIALIZED AGENT ASSESSMENT REPORTS:
    ${reportsPayload}

    Your goals:
    1. Resolve conflicts. (e.g. if compliance flags KYC issues but lending says approve, determine the proper course of action).
    2. Provide a definitive system outcome status. Output one of: [APPROVED], [DENIED], [FLAGGED_FOR_REVIEW] exactly in brackets.
    3. Calculate an aggregate Risk Score (0-100) based on agent concerns.
    4. Provide a structured, highly explainable banking decision memo including:
       - EXECUTIVE CONCLUSION
       - RISK MITIGATION / RESOLUTION STEPS
       - REGULATORY & OPERATIONAL DISCLOSURES
  `;

  const orchResult = await queryAgent(AgentId.ORCHESTRATOR, orchestratorAgent.systemPrompt, orchestratorPrompt);

  let status: "approved" | "denied" | "flagged_for_review" = "flagged_for_review";
  const orchTextLower = orchResult.text.toLowerCase();
  if (orchTextLower.includes("[approved]")) status = "approved";
  else if (orchTextLower.includes("[denied]")) status = "denied";
  else if (orchTextLower.includes("[flagged_for_review]")) status = "flagged_for_review";

  // Compute a blended Risk Score
  let calculatedRisk = customer.riskScore;
  const riskScores = Object.values(agentReports).map(r => r.recommendation === "deny" ? 30 : r.recommendation === "flag" ? 15 : 0);
  calculatedRisk = Math.min(95, Math.max(5, customer.riskScore + riskScores.reduce((a, b) => a + b, 0)));

  const finalReport: DecisionReport = {
    id: `decision-${Date.now()}`,
    customerId,
    customerName: customer.name,
    title: `${type.toUpperCase().replace("_", " ")} Audit Report`,
    type,
    date: new Date().toLocaleDateString("en-US", { month: "short", day: "numeric", year: "numeric" }),
    status,
    riskScore: calculatedRisk,
    orchestratorConclusion: orchResult.text,
    agentOpinions: agentReports as any,
    steps: steps.sort((a, b) => a.agentName.localeCompare(b.agentName))
  };

  res.json(finalReport);
});

